package com.blackducksoftware.integration.jira.config;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;

@XmlAccessorType(XmlAccessType.FIELD)
public class IdToNameMapping implements Serializable {

    private static final long serialVersionUID = -6879420109287472484L;

    @XmlElement
    private String id;

    @XmlElement
    private String name;

    public IdToNameMapping() {
    }

    public IdToNameMapping(String id, String name) {
        super();
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    @Override
    public String toString() {
        return "IdToNameMapping [id=" + id + ", name=" + name + "]";
    }
}
